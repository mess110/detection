package detect.api.models;

public class Region {

	private int tlx, tly, brx, bry;

	public Region() {

	}

	public int getTlx() {
		return tlx;
	}

	public void setTlx(int tlx) {
		this.tlx = tlx;
	}

	public int getTly() {
		return tly;
	}

	public void setTly(int tly) {
		this.tly = tly;
	}

	public int getBrx() {
		return brx;
	}

	public void setBrx(int brx) {
		this.brx = brx;
	}

	public int getBry() {
		return bry;
	}

	public void setBry(int bry) {
		this.bry = bry;
	}
}
